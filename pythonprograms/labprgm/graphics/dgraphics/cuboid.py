def areacub(l1,b1,h1):
    a=2*((l1*b1)+(b1*h1)+(l1*h1))
    print("Total Surface Area of the cuboid:",a)

def pericub(l1,b1,h1):
    p=4*(l1+b1+h1)
    print("The perimeter of the Cuboid:",p)

def lareacub(l1,b1,h1):
    la=2*(l1+b1)*h1
    print("The Lateral surface area of the cuboid is:",la)